export const iconExists = (name: string): boolean => {
  if (name in iconMap) return true;
  // Try kebab-case to PascalCase conversion
  const pascal = name
    .split('-')
    .map(w => w.charAt(0).toUpperCase() + w.slice(1))
    .join('');
  return pascal in iconMap;
};

export default iconMap;
